function estimateTokens(text) {
  const words = text.trim().split(/\s+/).filter(Boolean).length
  const chars = text.length
  return Math.round(Math.max(words * 1.55, chars / 3.7))
}

function pick(arr, index) {
  return arr[index % arr.length]
}

function renderFactParagraph(spec, block, offset) {
  const actor = pick(spec.actors, block + offset)
  const channel = pick(channels, block + offset * 2)
  const factA = pick(spec.facts, block * 3 + offset)
  const factB = pick(spec.facts, block * 5 + offset + 1)
  const factC = pick(spec.facts, block * 7 + offset + 2)
  const old = pick(spec.obsolete, block + offset)
  const note = pick(qualityNotes, block + offset)
  const localTime = `${String(8 + ((block + offset) % 11)).padStart(2, '0')}:${String((block * 7 + offset * 13) % 60).padStart(2, '0')}`

  return [
    `Фрагмент ${block}.${offset + 1}. Канал: ${channel}. Участник: ${actor}. Время записи: ${localTime}.`,
    `Подтвержденный смысловой узел: ${factA} Связанное уточнение: ${factB} Дополнительное условие, которое нельзя терять при сжатии: ${factC}`,
    `Отмененная или предварительная версия для сравнения: ${old} Ее нельзя переносить в финальный вывод как действующую норму, если рядом есть подтвержденное решение.`,
    `${note} В исходных материалах это повторено разными словами, потому что участники сверяли одинаковые условия в почте, таблице и устном протоколе.`,
    `Рабочая формулировка ${actor}: "Я подтверждаю не отдельную новую задачу, а тот же набор ограничений: ${factA.toLowerCase()} При подготовке краткой версии важно не заменить это общей фразой и не потерять срок, сумму, порог, исключение или ответственную сторону."`,
  ].join(' ')
}

function renderAuditTable(spec, block) {
  const rows = []
  for (let i = 0; i < 6; i += 1) {
    const fact = pick(spec.facts, block * 11 + i)
    const actor = pick(spec.actors, block + i)
    const risk = pick(spec.obsolete, block * 2 + i)
    rows.push(`строка ${block}-${i + 1}: владелец=${actor}; подтверждение="${fact}"; контроль черновика="${risk}"; статус=финальное подтверждение, если не указано иное.`)
  }
  return [
    `Контрольная таблица блока ${block}.`,
    rows.join(' '),
    'Таблица повторяет факты в машинно-похожем виде, но не добавляет новых обязательств сверх перечисленных подтверждений.',
  ].join(' ')
}

function renderTranscript(spec, block) {
  const lines = []
  for (let i = 0; i < 5; i += 1) {
    const actor = pick(spec.actors, block * 2 + i)
    const fact = pick(spec.facts, block * 13 + i)
    const second = pick(spec.facts, block * 17 + i + 3)
    lines.push(`${actor}: "В моей части остается так: ${fact} И отдельно фиксирую, что ${second.toLowerCase()} Это не заменяется ранними обсуждениями."`)
  }
  return [
    `Стенограмма сверки ${block}.`,
    lines.join(' '),
    `Модератор: "Если краткий пересказ будет нужен руководству, он должен убрать повторы, но сохранить финальные даты, численные пороги, исключения и выбранные варианты действий."`,
  ].join(' ')
}

function renderInput(spec) {
  const sections = [
    `Досье: ${spec.title}. Идентификатор: ${spec.id}. Область: ${spec.domain}.`,
    `Назначение пакета: длинный источник для проверки сжатия с минимальной потерей смысла. Материалы состоят из писем, протоколов, таблиц, черновиков и финальных уточнений. В пакете есть повторения и отмененные версии; они оставлены, чтобы проверить, отличает ли суммаризация действующие факты от устаревших.`,
    `Участники дела: ${spec.actors.join(', ')}.`,
    `Ниже идут первичные материалы. Они не являются готовой сводкой: один и тот же смысл повторяется в разных форматах, а часть ранних формулировок отменена более поздними подтверждениями.`,
  ]

  for (let i = 0; i < spec.facts.length; i += 1) {
    const actor = pick(spec.actors, i)
    sections.push(`Исходное подтверждение ${i + 1}. ${actor} фиксирует: ${spec.facts[i]} Это подтверждение появлялось в разных каналах и считается действующим, если рядом нет более позднего уточнения с тем же предметом.`)
  }

  for (let block = 1; estimateTokens(sections.join('\n\n')) < TARGET_INPUT_TOKENS; block += 1) {
    sections.push(`\n--- Пакет материалов ${block}: ${spec.title} ---`)
    sections.push(renderFactParagraph(spec, block, 0))
    sections.push(renderFactParagraph(spec, block, 1))
    sections.push(renderAuditTable(spec, block))
    sections.push(renderTranscript(spec, block))
    sections.push(renderFactParagraph(spec, block, 2))
    sections.push(`Конец пакета ${block}. Для смыслового сжатия этот пакет нужен как длинный контекст, а не как отдельная новая версия решения. Если он повторяет уже встречавшийся факт, повтор нужно схлопывать, сохраняя только действующее содержание.`)
  }

  return sections.join('\n\n')
}

